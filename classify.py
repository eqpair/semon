with open('/home/eq/semon/stocks_filtered.csv', encoding='utf-8-sig') as f:
    rows = list(csv.DictReader(f))

SKIP = {'기타', '창업투자', '부동산', '복합기업', '기타금융'}
result = {}  # code -> theme

for r in rows:
    code  = r['code']
    name  = r['name']
    group = r['group_name']

    if group in SKIP:
        continue
    # 우선주, ETN 등 제외
    if name.endswith('우') or name.endswith('우B') or name.endswith('우C') or '스팩' in name:
        continue
    if code.startswith('0') and len(code) == 6 and not code.isdigit():
        continue  # 특수코드 일부 제외 (ETF 등)

    theme = None

    # 반도체 세분화
    if group == "반도체와반도체장비":
        if code in SEMICON_MAKERS:
            theme = "반도체_제조"
        elif code in SEMICON_EQUIP:
            theme = "반도체_장비"
        elif code in SEMICON_MAT:
            theme = "반도체_소재부품"
        else:
            # 이름 기반 추가 분류
            if any(k in name for k in ["소재","케미칼","켐","씨엔씨","물질","가스"]):
                theme = "반도체_소재부품"
            elif any(k in name for k in ["장비","IPS","테크","엔지","시스템","솔루션","로봇"]):
                theme = "반도체_장비"
            else:
                theme = "반도체_소재부품"

    # 제약 세분화
    elif group == "제약":
        if code in CMO_CDMO_SET:
            theme = "CMO_CDMO"
        elif code in LARGE_PHARMA:
            theme = "대형제약"
        elif code in BIO_TECH:
            theme = "바이오_기술이전"
        else:
            theme = "중소제약"

    # 생물공학 세분화
    elif group == "생물공학":
        if code in BIO_TECH:
            theme = "바이오_기술이전"
        elif code in BIO_CLINICAL:
            theme = "바이오_임상단계"
        else:
            theme = "바이오_임상단계"

    # 화학 세분화
    elif group == "화학":
        if code in BATTERY_MAT:
            theme = "2차전지_소재부품"
        elif code in PETROCHEM:
            theme = "석유화학"
        elif code in SPEC_CHEM:
            theme = "특수화학·소재"
        else:
            if any(k in name for k in ["배터리","이차전지","2차전지","리튬","음극","양극","전해"]):
                theme = "2차전지_소재부품"
            elif any(k in name for k in ["석유","케미칼","화학","정밀","솔루션"]):
                theme = "석유화학"
            else:
                theme = "특수화학·소재"

    # 기계 세분화
    elif group == "기계":
        if code in ROBOT_SET or any(k in name for k in ["로봇","Robot","로보"]):
            theme = "로봇"
        elif any(k in name for k in ["에너지","발전","풍력","태양","원자력"]):
            theme = "신재생에너지"
        else:
            theme = "조선"  # 중공업/기계 일반은 조선 테마와 연관 많음
            # 실제로는 별도 테마 필요하지만 일단 유사 테마로

    # 전기제품 세분화 (2차전지)
    elif group == "전기제품":
        if code in BATTERY_CELL:
            theme = "2차전지_셀"
        elif code in BATTERY_EQ:
            theme = "2차전지_소재부품"
        elif any(k in name for k in ["연료전지","퓨얼셀","수소"]):
            theme = "신재생에너지"
        elif any(k in name for k in ["배터리","2차전지","이차전지","리튬","양극","음극","전해"]):
            theme = "2차전지_소재부품"
        else:
            theme = "2차전지_소재부품"

    # 전기장비 세분화
    elif group == "전기장비":
        if code in DEFENSE_SET:
            theme = "방산"
        elif any(k in name for k in ["전선","케이블"]):
            theme = "전력기기"
        else:
            theme = "전력기기"

    # 자동차부품 세분화
    elif group == "자동차부품":
        if any(k in name for k in ["타이어","Tire"]):
            theme = "자동차_부품"  # 타이어는 별도 테마지만 통합
        else:
            theme = "자동차_부품"

    # 디스플레이 장비/부품
    elif group == "디스플레이장비및부품":
        if code in DISP_OLED:
            theme = "IT부품"
        else:
            theme = "IT부품"

    # 화장품 세분화
    elif group == "화장품":
        if any(k in name for k in ["콜마","코스맥스","씨앤씨","코스메카","한국콜마"]):
            theme = "화장품_ODM"
        else:
            theme = "화장품_브랜드"

    # 나머지는 기본 매핑
    elif group in GROUP_MAP:
        theme = GROUP_MAP[group]

    if theme:
        result[code] = {"name": name, "theme": theme, "group": group}

# 테마별 집계
themes = {}
for code, info in result.items():
    t = info["theme"]
    if t not in themes:
        themes[t] = []
    themes[t].append((code, info["name"]))

print(f"총 분류된 종목: {len(result)}개")
print(f"테마 수: {len(themes)}개\n")
print("="*60)
for t in sorted(themes.keys()):
    stocks = themes[t]
    print(f"\n[{t}] {len(stocks)}개")
    for code, name in stocks[:5]:
        print(f"  {code} {name}")
    if len(stocks) > 5:
        print(f"  ... 외 {len(stocks)-5}개")

